#include <iostream>
using namespace std;
int main() {
    int a = 3, b= 4;
    cout << "operators in C++" << endl;
    cout << "Arithmatic Operator" << endl;
    cout << "The value of a + b " << a + b << endl;
    cout << "The value of a - b " << a - b << endl;
    cout << "The value of a * b " << a * b << endl;
    cout << "The value of a / b " << a / b << endl;
    cout << "The value of a % b " << a % b << endl;
    cout << "The value of a++ " << a++ << endl;
    cout << "The value of a-- " << a-- << endl;
    cout << "The value of --a " << --a << endl;
    cout << "The value of ++a " << ++a << endl;

    cout << "Assingment Operator" << endl;
    return 0;
}